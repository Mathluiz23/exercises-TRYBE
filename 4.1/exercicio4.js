// Faça um programa que, dado um valor definido numa variável, retorne "positive" se esse valor for positivo, "negative" se for negativo e "zero" caso contrário.



let  number = -5;

if (number > 0) {
  console.log('O valor é positivo = '+ number +'');
}

else if (number < 0) {
  console.log('O valor é negativo = '+ number +'');
}

else {
  console.log('O valor é zero = '+ number +'');
}
